// Bottom-sheet **Overlay Contract** (Volume System v2). Bit us 3x as a
// per-component fix; now enforced at the primitive so every future sheet
// inherits it:
//   1. Height-clamped to the viewport via inline dvh + maxHeight (an inline
//      style beats the base variant `data-[side=bottom]:h-auto` that previously
//      let sheets size to content and push their headers above the top edge).
//   2. Flex column so consumers get a sticky header + a single scroll region
//      + a pinned footer for free.
//   3. Persistent ≥44px close affordance, reachable from any scroll position.
//      Use showCloseButton={false} only when a consumer provides its own.
function SheetContent({
  className,
  children,
  side = "right",
  showCloseButton = true,
  style,
  ...props
}: SheetPrimitive.Popup.Props & {
  side?: "top" | "right" | "bottom" | "left"
  showCloseButton?: boolean
}) {
  const isBottom = side === "bottom" || side === "top";
  // Inline height clamp wins against the variant `h-auto` class. Consumers can
  // still override by passing their own style.height — but they shouldn't need to.
  const enforcedStyle: React.CSSProperties = isBottom
    ? { height: "90dvh", maxHeight: "90dvh", ...style }
    : { ...style };
  return (
    <SheetPortal>
      <SheetOverlay />
      <SheetPrimitive.Popup
        data-slot="sheet-content"
        data-side={side}
        style={enforcedStyle}
        className={cn(
          "fixed z-50 flex flex-col bg-popover bg-clip-padding text-sm text-popover-foreground shadow-lg transition duration-200 ease-in-out data-ending-style:opacity-0 data-starting-style:opacity-0 data-[side=bottom]:inset-x-0 data-[side=bottom]:bottom-0 data-[side=bottom]:border-t data-[side=bottom]:data-ending-style:translate-y-[2.5rem] data-[side=bottom]:data-starting-style:translate-y-[2.5rem] data-[side=left]:inset-y-0 data-[side=left]:left-0 data-[side=left]:h-full data-[side=left]:w-3/4 data-[side=left]:border-r data-[side=left]:data-ending-style:translate-x-[-2.5rem] data-[side=left]:data-starting-style:translate-x-[-2.5rem] data-[side=right]:inset-y-0 data-[side=right]:right-0 data-[side=right]:h-full data-[side=right]:w-3/4 data-[side=right]:border-l data-[side=right]:data-ending-style:translate-x-[2.5rem] data-[side=right]:data-starting-style:translate-x-[2.5rem] data-[side=top]:inset-x-0 data-[side=top]:top-0 data-[side=top]:border-b data-[side=top]:data-ending-style:translate-y-[-2.5rem] data-[side=top]:data-starting-style:translate-y-[-2.5rem] data-[side=left]:sm:max-w-sm data-[side=right]:sm:max-w-sm",
          className
        )}
        {...props}
      >
        {children}
        {showCloseButton && (
          <SheetPrimitive.Close
            data-slot="sheet-close"
            aria-label="Close"
            render={
              <button
                type="button"
                className="absolute top-2 right-2 z-10 min-h-[44px] min-w-[44px] inline-flex items-center justify-center rounded-full text-[color:var(--pump-text-dim)] hover:text-[color:var(--pump-text)] active:scale-95 transition"
              />
            }
          >
            <XIcon className="w-5 h-5" />
            <span className="sr-only">Close</span>
          </SheetPrimitive.Close>
        )}
      </SheetPrimitive.Popup>
    </SheetPortal>
  )
}

